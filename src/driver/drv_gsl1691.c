/// @brief GSL1691 touch pad init.
/// @param func Interrupt callback function, called when EXTINT gpio rasing/falling.
void Gsl1691Init(void)
{
    I2cInit();
    Gsl1691InitSequence();
}


/// @brief GSL1691 open.
void Gsl1691Open(void)
{
    I2cInit();
    Gsl1691InitSequence();
}


/// @brief Get touch status, including touch state, X/Y coordinate.
/// @param status TouchStatus struct addr.
int32_t Gsl1691GetStatus(TouchStatus_t *status)
{
    uint8_t regAddr, readBuff[44];
    regAddr = 0x80;
    I2cSendAndReceiveData(GSL1691_I2C_ADDR, &regAddr, 1, readBuff, 44);
    //PrintArray("readBuff", readBuff, 44);
    status->touch = readBuff[0] > 0 ? true : false;
    status->x = ((readBuff[7] & 0x0F) << 8) + readBuff[6];
    memcpy(&(status->y), &readBuff[4], 2);
    //printf("x=%d,y=%d,status=%d\n", status->x, status->y, status->touch);

    return SUCCESS_CODE;
}



static void Gsl1691InitSequence(void)
{
    Gsl1691ClearReg();
    Gsl1691ResetCore();
    Gsl1691UpdateFirmware();
    Gsl1691StartCore();
    Gsl1691ResetCore();
    Gsl1691StartCore();
    Gsl1691CheckData();
}

static void Gsl1691Reset(void)
{
    uint8_t sendBuf[2];
    sendBuf[0] = 0xE0;
    sendBuf[1] = 0x88;
    I2cSendData(GSL1691_I2C_ADDR, sendBuf, 2);
    UserDelay(20);
}

static void Gsl1691ClearReg(void)
{
    uint8_t sendBuf[2];

    Gsl1691Reset();
    sendBuf[0] = 0x80;
    sendBuf[1] = 0x03;
    I2cSendData(GSL1691_I2C_ADDR, sendBuf, 2);
    UserDelay(5);
    sendBuf[0] = 0xE4;
    sendBuf[1] = 0x04;
    I2cSendData(GSL1691_I2C_ADDR, sendBuf, 2);
    UserDelay(5);
    sendBuf[0] = 0xE0;
    sendBuf[1] = 0x00;
    I2cSendData(GSL1691_I2C_ADDR, sendBuf, 2);
    UserDelay(20);
}


static void Gsl1691ResetCore(void)
{
    uint8_t sendBuf[5];

    Gsl1691Reset();
    sendBuf[0] = 0xE4;
    sendBuf[1] = 0x04;
    I2cSendData(GSL1691_I2C_ADDR, sendBuf, 2);
    UserDelay(10);
    sendBuf[0] = 0xBC;
    sendBuf[1] = 0x00;
    sendBuf[2] = 0x00;
    sendBuf[3] = 0x00;
    sendBuf[4] = 0x00;
    I2cSendData(GSL1691_I2C_ADDR, sendBuf, 5);
    UserDelay(10);
}


static void Gsl1691StartCore(void)
{
    uint8_t sendBuf[2];

    sendBuf[0] = 0xE0;
    sendBuf[1] = 0x00;
    I2cSendData(GSL1691_I2C_ADDR, sendBuf, 2);
    UserDelay(10);
}


static void Gsl1691CheckData(void)
{
    uint8_t checkData[4], tempU8;

    tempU8 = 0xB0;
    memset(checkData, 0x88, 4);
    I2cSendAndReceiveData(GSL1691_I2C_ADDR, &tempU8, 1, checkData, 4);
    PrintArray("0xB0", checkData, 4);
}


